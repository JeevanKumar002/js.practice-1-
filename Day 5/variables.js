// The "Let" (can change)
let movie = "Chaava";
console.log(movie)
movie = "Dragon"; //changing the "movie" variable data
console.log(movie) //Changed

// The "Var" ( Global but older)
var actress = "Rashmika"; //this blue-underline is a warning from vs code to don't use "var"
console.log (actress)
actress = "Mrunal"; //changing the "actress" variable data
console.log (actress) //changed

// The "Const" (Fixed 'can't change')
const actor = "vicky kaushal";
console.log(actor) 
actor = "akshay"; //Changing "actor variable value"
console.log(actor) // Didn't change